package com.xiangwei.controller;

public class producer_info {
	
	private String producer_cityString;
	private String producer_language;
	private int producer_credit;
	
	public String getProducer_cityString() {
		return producer_cityString;
	}
	public void setProducer_cityString(String producer_cityString) {
		this.producer_cityString = producer_cityString;
	}
	public String getProducer_language() {
		return producer_language;
	}
	public void setProducer_language(String producer_language) {
		this.producer_language = producer_language;
	}
	public int getProducer_credit() {
		return producer_credit;
	}
	public void setProducer_credit(int producer_credit) {
		this.producer_credit = producer_credit;
	}
	
	

}
